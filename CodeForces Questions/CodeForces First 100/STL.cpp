    cout << "Capacity " << v.capacity() << endl; // now the size doubles from 2 to 4 since dynamically expansion of memory to fit in the elements
    cout << "Size " << v.size() << endl;
    cout << "element at index 2" << v.at(2) << endl;
    cout << "First Element " << v.front() << endl; // check first element
    cout << "Last Element " << v.back() << endl;   // check last element
    cout << "Before pop" << endl;
    for (int i : v)
    {
        cout << i << endl;
    }
    v.pop_back();
    cout << "After pop" << endl;
    for (int i : v)
    {
        cout << i << endl;
    }
